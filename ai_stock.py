def predict(symbol, data):
    stock_data = data[data["Symbol"] == symbol].copy()
    stock_data = add_indicators(stock_data)

    prices = stock_data["Close"].values
    current_price = prices[-1]

    # --- LSTM only ---
    lstm_price = trainLSTM(prices, lookback=lstm_lookback, epochs=lstm_epochs)
    if lstm_price is None:
        lstm_price = current_price  # fallback to current if not enough data

    predicted_price = lstm_price

    change_pct = ((predicted_price - current_price) / current_price) * 100

    if change_pct > 0.3:
        trend = "Upward 📈"
    elif change_pct < -0.3:
        trend = "Downward 📉"
    else:
        trend = "Stable ➡️"

    rsi  = stock_data["RSI"].iloc[-1]
    macd = stock_data["MACD"].iloc[-1]

    explanation = get_llm_explanation(
        symbol=symbol,
        current_price=round(float(current_price), 2),
        lstm_price=round(float(lstm_price), 2),      # ✅ removed lr_price
        change_pct=round(float(change_pct), 2),
        trend=trend,
        rsi=rsi,
        macd=macd
    )

    return {
        "symbol": symbol,
        "current_price": round(float(current_price), 2),
        "predicted_price": round(float(predicted_price), 2),  # ✅ removed lr_predicted
        "change_percent": round(float(change_pct), 2),
        "trend": trend,
        "rsi": round(float(rsi), 2),
        "macd": round(float(macd), 2),
        "AI_explanation": explanation,
        "history": stock_data  # for charts
    }

