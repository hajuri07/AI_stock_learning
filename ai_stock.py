def add_indicators(df):
    """Adds RSI, MACD, Bollinger Bands, EMA to a single stock df"""
    df = df.copy()

    # RSI - momentum
    df['RSI'] = ta.momentum.RSIIndicator(df['Close'], window=14).rsi()

    # MACD - trend
    macd = ta.trend.MACD(df['Close'])
    df['MACD'] = macd.macd()
    df['MACD_signal'] = macd.macd_signal()

    # Bollinger Bands - volatility
    bb = ta.volatility.BollingerBands(df['Close'], window=20)
    df['BB_high'] = bb.bollinger_hband()
    df['BB_low']  = bb.bollinger_lband()
    df['BB_mid']  = bb.bollinger_mavg()

    # EMA 20 - trend baseline
    df['EMA_20'] = ta.trend.EMAIndicator(df['Close'], window=20).ema_indicator()

    # ATR - volatility measure
    df['ATR'] = ta.volatility.AverageTrueRange(
        df['High'], df['Low'], df['Close']
    ).average_true_range()

    df.dropna(inplace=True)
    return df

from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Dense,LSTM,Dropout
from sklearn.preprocessing import MinMaxScaler

def trainLSTM(prices,lookback=lstm_lookback,epochs=lstm_epochs):
  scaler=MinMaxScaler()
  scaled=scaler.fit_transform(prices.reshape(-1,1))
  x,y=[],[]
  for i in range(lookback,len(scaled)):
    x.append(scaled[i-lookback:i,0])
    y.append(scaled[i,0])
  x=np.array(x).reshape(-1,lookback,1)
  y=np.array(y)


  model=Sequential([
      LSTM(64, return_sequences=True, input_shape=(lookback,1)),
      Dropout(0.2),
      LSTM(64),
      Dense(1)
  ])
  model.compile(optimizer='adam',loss='mse')
  model.fit(x,y,epochs=lstm_epochs,batch_size=15,verbose=0)
  last_seq = scaled[-lookback:].reshape(1, lookback, 1)
  pred_scaled = model.predict(last_seq, verbose=0)[0][0]

  predicted_price = scaler.inverse_transform([[pred_scaled]])[0][0]
  return predicted_price

